package com.zkk.pojo;

import java.util.Date;

public class Tab {
    private String uid;
    private String uname;
    private int grade;
    private String cid;
    private String cname;
    private Date jzdate;
    private String com;
    private String exi;

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    public Date getJzdate() {
        return jzdate;
    }

    public void setJzdate(Date jzdate) {
        this.jzdate = jzdate;
    }

    public String getCom() {
        return com;
    }

    public void setCom(String com) {
        this.com = com;
    }

    public String getExi() {
        return exi;
    }

    public void setExi(String exi) {
        this.exi = exi;
    }


}
